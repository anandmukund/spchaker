package com.airtel.spchacker.dto;

public class UserDetails {

	private String name;
	
	private String dept;
	
	private int age;
	
	private byte[] documentProff;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public byte[] getDocumentProff() {
		return documentProff;
	}

	public void setDocumentProff(byte[] documentProff) {
		this.documentProff = documentProff;
	}
	
	
	
}
